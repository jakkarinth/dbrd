ชื่อหน่วยงาน<br><br>
งานวิจัยและพัฒนา มหาวิทยาลัยเทคโนโลยีราชมงคลอีสาน วิทยาเขตสุรินทร์<br>
<br>
ที่ตั้ง<br><br>
เลขที่ 145 หมู่ 15 ถนนสุรินทร์ – ปราสาท ตำบลนอกเมือง อำเภอเมือง จังหวัดสุรินทร์ 32000 หมายเลขโทรศัพท์ 044-153090 ต่อ 140 หมายเลขโทรสาร 044-153064<br>
<br>
ประวัติความเป็นมาโดยย่อ<br><br>
18 มกราคม พ.ศ. 2548 สถาบันเทคโนโลยีราชมงคลทุกแห่ง ได้รับการยกฐานะเป็นมหาวิทยาลัย-เทคโนโลยีราชมงคล โดยมี 9 แห่งเพื่อให้สถานศึกษาของรัฐที่จัดการที่ศึกษาระดับปริญญาเป็นนิติบุคคล<br>
ดำเนินการได้โดยอิสระสามารถพัฒนาระบบการบริหารจัดการที่มีความคล่องตัว มีเสรีภาพทางวิชาการภายใต้การกำกับดูแลของสภามหาวิทยาลัย เป็นสถาบันอุดมศึกษาของรัฐด้านวิชาชีพ และ เทคโนโลยี <br>
ที่มีวัตถุประสงค์ ให้การศึกษาส่งเสริมวิชาการและวิชาชีพขั้นสูง ที่มุ่งเน้นการปฏิบัติ ทำการสอน ทำการวิจัยผลิตครูวิชาชีพให้บริการวิชาการ ด้านวิทยาศาสตร์และเทคโนโลยีและทำนุบำรุงศาสนา <br>
ศิลปวัฒนธรรม โดยให้ผู้สำเร็จการศึกษาทางด้านอาชีวศึกษามีโอกาสในการศึกษาต่อด้านวิชาชีพเฉพาะทาง ในระดับปริญญาเป็นหลัก ดังนั้น มหาวิทยาเทคโนโลยีราชมงคลอีสาน ซึ่งประกอบด้วยวิทยาเขต <br>
ภาคตะวันออกเฉียงเหนือ นครราชสีมา วิทยาเขตขอนแก่น วิทยากาฬสินธุ์ วิทยาเขตสกลนคร และวิทยาเขตสุรินทร์ โดยมีโครงสร้างการบริหารในระดับวิทยาเขตฯ โดยการรวมกลุ่มงาน อันประกอบด้วย ฝ่ายบริหารงานทั่วไป ฝ่ายวิจัยและพัฒนา ฝ่ายบริการ-วิชาการและถ่ายทอดเทคโนโลยี และฝ่ายบ่มเพาะวิสาหกิจและสิทธิบัตรเป็น “กองวิจัยและพัฒนา” (หน่วยงานภายในที่มหาวิทยาลัยจัดตั้งขึ้น) และในปี พ.ศ. 2552 ได้เปลี่ยนเป็น “งานวิจัยและพัฒนา”
<br><br>
