<br>ปณิธาน<br><br>
พร้อมทำงาน ให้บริการอย่างมิตร จิตมุ่งมั่นตั้งใจ ใฝ่ประสานทุกองค์กร มีส่วนร่วม พร้อมนำองค์ความรู้ สู่การพัฒนา<br>

<br>นโยบาย<br><br>
– ส่งเสริม สนับสนุนงานวิจัย และสิ่งประดิษฐ์ที่มีคุณภาพ เพื่อตอบสนองความต้องการของสังคม โดย เน้นการถ่ายทอดเทคโนโลยีที่เหมาะสมอย่างต่อเนื่อง – บริการทางวิชาการ และถ่ายทอดเทคโนโลยีที่เหมาะสมสู่สังคม – พัฒนาศักยภาพด้านการวิจัยของบุคลากรทุกระดับ – พัฒนาระบบบริหารงานของกองวิจัยและพัฒนา ให้มีประสิทธิภาพ<br>

<br>วิสัยทัศน์<br><br>
งานวิจัยและพัฒนา มุ่งมั่นพัฒนา ส่งเสริมงานวิจัย และสิ่งประดิษฐ์ งานบริการทางวิชาการและถ่ายทอดเทคโนโลยีสู่ชุมชน อย่างมีประสิทธิภาพและยั่งยืน<br>

<br>เป้าประสงค์<br><br>
เผยแพร่ และถ่ายทอดผลงานวิจัยและสิ่งประดิษฐ์สู่สังคม เพื่อปรับใช้ให้เป็นประโยชน์อย่างเหมาะสม<br><br>

<br>ภารกิจ<br><br>
งานวิจัยและพัฒนา มีภารกิจหลัก คือ – งานด้านการบริหารงานทั่วไป – งานด้านควบคุม ดูแล และประสานงานด้านวิจัย – งานด้านให้การบริการทางวิชาการและถ่ายทอดเทคโนโลยี – งานด้านบ่มเพาะวิสาหกิจและสิทธิบัตร<br>
